import java.util.Scanner;

public class 백준1_6 {
    public static void main(String[] args) {
        Scanner s = new Scanner(System.in);
        int a, b;

        a = s.nextInt();
        b = s.nextInt();

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
    
}
